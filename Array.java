public class Array {
    public static void main(String[] args) {
        int nums[] = new int[4]; //all the values are zero
        nums[0] = 4;
        nums[1] = 8;
        nums[2] = 3;
        nums[3] = 9;

//        nums[1] = 6;
        for (int i=0;i<=3;i++) {
            System.out.println(nums[i]);
        }
// used loops for printing all the values in an array!
    }
}
